package StringsMethods;

public class indexOfMethod { //returns the index of the first occurrence of specified string

    public static void main(String[] args) {

        String buddhistQuote  = "Don't rush anything. When the time is right it'll happen.";

        System.out.println(buddhistQuote.indexOf("happen"));
        System.out.println(buddhistQuote.indexOf("right"));
        System.out.println(buddhistQuote.indexOf("time"));

        

    }
    
}
