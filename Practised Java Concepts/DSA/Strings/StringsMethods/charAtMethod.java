package StringsMethods;

/*
 
The String class has a set of built-in-methods that can be used on strings.

*/

public class charAtMethod {

    public static void main(String[] args) {

        String name = "Bhavani";
        char character =  name.charAt(3); //charAt() method, returns character at specified index.
        System.out.println(character);
        

    }
    
}
